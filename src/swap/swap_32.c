#include <stdint.h>

uint32_t swap_32(uint32_t value, uint8_t swap)
{
	return (swap ? (value >> 24) |
		((value & 0xFF0000) >> 8) |
		((value & 0xFF00) << 8) |
		(value << 24) : value);
}
